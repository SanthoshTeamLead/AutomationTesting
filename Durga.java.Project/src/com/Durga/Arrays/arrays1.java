package com.Durga.Arrays;

public class arrays1 {

	public static void main(String[] args) {
		//Single Dimenstion Array 
		int[]  x ;
		int   []y;
		int   z[];
		
		//Double Dimenstion Array 
		int[][]  a,b;
		int      [][]c, d;
		int      e,f[][];
		int[]   []g,h[];
		int[]   []i,j[];
		
		
		byte[] by = new byte[3];
		System.out.println(by.getClass().getName());
		
		short[] sh = new short[5];
		System.out.println(sh.getClass().getName());
		
		int[][] age = new int[3][5];
		System.out.println(age.getClass().getName());
		
		long[][] number = new long[10][10];
		System.out.println(number.getClass().getName());

		
		
		

	}

}
