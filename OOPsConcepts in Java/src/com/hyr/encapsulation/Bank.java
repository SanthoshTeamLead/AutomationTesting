package com.hyr.encapsulation;

public class Bank {
	private String Name;
	private long AccountNo;
	
	public void setName(String Name) {
		this.Name = Name;
	}
	public String getName() {
		return Name;
	}
	
	public void setAccountNo(long AccountNo) {
		this.AccountNo = AccountNo;
	}
	public long getAccountNo() {
		return AccountNo;
	}
	
	
	

}
